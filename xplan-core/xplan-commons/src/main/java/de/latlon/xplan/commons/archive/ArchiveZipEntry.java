package de.latlon.xplan.commons.archive;

import java.util.zip.ZipEntry;

/**
 * An {@link ArchiveEntry} encapsulating an {@link ZipEntry}.
 *
 * @author <a href="mailto:goltz@lat-lon.de">Lyn Goltz</a>
 * @version $Revision: $, $Date: $
 */
public class ArchiveZipEntry implements ArchiveEntry {

	private final ZipEntry zipEntry;

	/**
	 * @param zipEntry never <code>null</code>
	 */
	public ArchiveZipEntry(ZipEntry zipEntry) {
		this.zipEntry = zipEntry;

	}

	@Override
	public String getName() {
		return zipEntry.getName();
	}

}
