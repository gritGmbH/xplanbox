package de.latlon.xplan.validator.web.shared;

import java.io.Serializable;

/**
 * Encapsulates a validation profile.
 *
 * @author <a href="mailto:goltz@lat-lon.de">Lyn Goltz</a>
 * @version $Revision: $, $Date: $
 */
public class ValidationProfile implements Serializable {

	private static final long serialVersionUID = -4058602240732626680L;

	private String id;

	private String name;

	private String description;

	public ValidationProfile() {
	}

	public ValidationProfile(String id, String name, String description) {
		this.id = id;
		this.name = name;
		this.description = description;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	@Override
	public String toString() {
		return "ValidationProfile{" + "id='" + id + '\'' + ", name='" + name + '\'' + ", description='" + description
				+ '\'' + '}';
	}

}
